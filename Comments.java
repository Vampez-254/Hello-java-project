public class Comments {
    public static void main(String[]args) {
        /*comments in java
        * We have three types of comments in java
        * 1. C++ Style comments -> //
        * 2. c style comments
        * 3. special javadoc comments */
       //1. c++ comments style
        System.out.println("we use// for c++ comments style");
        /*2. c comment style */
        System.out.println("we use /**/ for c comments style");
        /**
         * 3.special javadoc comment
         */
        System.out.println("3.we use /** */ for special javadoc comments");
    }
}
